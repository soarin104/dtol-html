<script>
	$(function(){
		
		//현재임원 - 역대임원
		$(".exe .tab ul li").on("click",function(){
			$(this).addClass("on");
			$(this).siblings().removeClass("on");

			let exeIdx = $(this).index();
			$(".exe .conBox > div").eq(exeIdx).show();
			$(".exe .conBox > div").eq(exeIdx).siblings().hide();
		})

		//역대임원 연도선택
		$(".exe .conBox .oldExe select").on("change",function(){
			let exeVal = $(this).val();
			$(".exe .conBox .oldExe .imgBox img[alt='"+exeVal+"']").show();
			$(".exe .conBox .oldExe .imgBox img[alt='"+exeVal+"']").siblings().hide();
		})
	})
</script>