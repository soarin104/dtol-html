						③ 제1항의 규정에 의한 청구가 있을 때에는 이사장은 총회 회의일의 7일전에 당해 임원에게 개선사유를 통지하고 총회에서 의견을 진술할 기회를 주어야 한다.<br>
						<strong>제58조(임원의 성실의무)</strong> 임원은 법령, 정관 및 규약과 총회의 의결사항을 준수하고 조합을 위하여 성실히 그 직무를 수행하여야 한다.<br>
						<strong>제59조(임원의 보수 등)</strong><br>
						① 임원(비상근 포함)에 대하여 규정이 정하는 바에 따라 여비 등 조합업무와 관련된 비용을 실비 범위 내에서 지급할 수 있다.<br>
						② 이사장에 대하여는 총회의 의결을 거쳐 규정으로 정하는 경우 조합업무와 관련된 활동수당을 지급할 수 있다.<br>
						<strong>제60조(정관 기타 서류비치)</strong><br>
						① 이사장은 정관, 규약, 규정과 총회 및 이사회의 의사록, 조합원명부를 조합에 비치하여야 한다.<br>
						② 조합원과 조합의 채권자는 언제든지 이사장에 대하여 제1항의 서류의 열람 또는 사본을 청구할 수 있다.<br>
