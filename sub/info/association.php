						⑤ 조합은 매회계년도마다 조합원에 대하여 제1항과 제2항의 규정에 의한 조합원 자격을 확인하여야 한다.<br>
						<strong>제10조(특별조합원) </strong><br>
						조합은 업무구역 안에 주소를 두고 있는 출판물의 생산, 유통, 판매 관련 단체 등을 특별조합원으로 할 수 있다.<br>
						<strong>제11조(가입)</strong><br>
						① 조합원의 자격을 가진 자로서 조합에 가입하고자 하는 자는 소정의 서식을 갖추어
						가입신청서를 제출하여야 한다.<br>
						② 제1항의 규정에 의한 신청서가 접수되었을 때에는 조합은 신청인의 조합원 자격을
						확인하고 가입의 가부를 결정하여 신청서를 접수한 날부터 2주 이내에 신청자에게 서면으로 통지하여야 한다.<br>
						③ 제2항의 규정에 의하여 가입통지를 받은 자는 조합에 가입할 자격을 가지며 인수출자좌수에 대한 금액과 가입금을 지정한 기일 내에 조합에 납부함으로써 조합원이 된다.<br>
						④ 조합원이 될 자격을 가진 자가 조합에 가입하고자 할 때에는 조합은 정당한 이유없이 이를 거부하거나 
						또는 현재의 조합원의 가입당시에 붙인 것 보다 불리한 조건을 붙여서는 아니 된다.<br>
						⑤ 제16조에 따라 탈퇴하거나 제17조 제1항 제3호에 따라 제명된 자로서 탈되 또는 제명된 날부터 2년이 경과되지 아니한 자는 조합에 다시 가입할 수 없다.<br>
						<strong>제12조(가입금)</strong><br>
						① 조합은 조합에 가입하는 자로부터 소정의 가입금을 징수할 수 있다.<br>
						② 제1항의 가입금의 금액 및 징수에 관하여 필요한 사항은 따로 규약으로 정한다.<br>
						<strong>제13조(상속가입)</strong><br>
						① 사망한 조합원의 상속인으로서 조합원이 될 자격을 가진 자는 상속개시 후 3월이내에 
						가입신청을 하였을 때에는 제11조의 규정에 불구하고 상속이 개시된 때로부터 조합원이 된 것으로 본다. <br>
						이 경우 상속인인 조합원은 피상속인의 지분에 관하여 사망한 조합원의 권리의무를 승계 한다.<br>
						② 제1항의 규정에 의한 상속인이 수인인 때에는 가입신청을 하고자 하는 자는 다른 상속인의 동의서를 제출하여야 한다.<br>
						<strong>제14조(출자금액과 그 납입방법)</strong><br>
						① 조합원은 1좌 이상의 출자를 하여야 하며 1좌의 금액은 10만원으로 한다. 다만, 이사회는 각 조합원의 출자배정 기준을 정하여 이를 조합원에게 권유할 수 있다.<br>
						② 한 조합원의 출자좌수는 출자 총좌수의 100분의 20을 초과해서는 안 된다.<br>
						③ 인수출자좌수에 대한 출자금은 인수한 날로부터 3주일 이내에 조합에 전액을 일시에 납입하여야 한다.<br>
						④ 조합은 조합원이 출자금을 납입하였을 때에는 출자금 납입을 증명하는 출자증서를 교부하여야 한다.<br>
						⑤ 출자좌수를 증가하고자 하는 조합원은 소정의 절차를 밟아 이사회의 의결에 의하여 증좌납입을 할 수 있다. 이 경우에는 제3항의 규정을 준용한다.<br>
						⑥ 조합원이 휴업한 때, 사업의 일부를 폐지한 때 또는 기타 부득이한 사유가 있을 때에는 
						당해 조합원의 감자신청에 의하여 이사회에서 정하는 바에 따라 사업년도 말에 한하여 그 조합원의 출자좌수를 감소할 수 있다.<br>
						⑦ 조합원은 출자금의 납입에 관하여 상계로써 조합에 대항하지 못한다.<br>
						⑧ 출자는 현물로도 할 수 있다. 이 경우 현물 출자자는 제1회 출자의 납입기일에 출자의 목적인 재산의 전부를 조합 또는 조합에서 지정한 장소에 납입하여야 한다.<br>
						⑨ 조합은 총회의 결의에 의하여 조합원에게 배당되는 잉여금의 일부 또는 전부를 조합원으로 하여금 회전 출자하게 할 수 있다. 이 경우에는 제7항의 규정을 준용한다.<br>
						⑩ 조합원의 책임은 그 출자액을 한도로 한다.<br>
						<strong>제15조(조합비, 사용료 및 수수료)</strong><br>
						① 조합은 사업운영을 위하여 다음 각 호와 같은 경비를 징수한다.<br>
						1. 기본조합비<br>
						2. 관리조합비<br>
						3. 공급조합비<br>
						4. 사업자금조합비<br>
						5. 사무실 이용관리비<br>
						6. 공동시설사용료<br>
						7. 특별조합비<br>
						② 제1항의 조합비, 특별조합비, 사용료 및 수수료 및 과태료의 금액과 그 징수일시,
						방법은 따로 규약으로 정한다. 다만 경비 등을 경감하고자 할 때에는 이사회의 의결이 있어야 한다.<br>
						③ 조합원은 경비 등의 지급에 관하여 상계로써 조합에 대항하지 못한다.<br>
						④ 조합원에 대한 조합비, 사용료 및 수수료 및 과태료 등의 산정기준에 변경이 있을때에도 이미 부과된 금액은 이를 변경하지 아니한다.<br>
						⑤ 조합은 조합원이 부과금 납입의 의무를 지정한 기한까지 이행하지 아니할 때에는
						그 기한경과 후 미납입금액에 대하여 매일 500분의 1 이내에 상당하는 금액을 징수할 수 있다.<br>
						<strong>제16조(임의탈퇴)</strong><br>
						① 조합원은 30일전에 예고하고 예고기간 경과 후 탈퇴할 수 있다.<br>
						② 제1항의 예고는 그 취지를 기재한 서면으로써 하여야 한다.<br>
						<strong>제17조(법정탈퇴)</strong><br>
						① 조합원은 다음 각 호의 1에 해당하는 사유가 발생하였을 때에는 자연히 탈퇴한다.<br>
						1. 조합원이 될 자격의 상실<br>
						2. 사망 또는 해산<br>
						3. 제명<br>
						4. 파산의 선고<br>
						② 제1항 제1호에 의한 자격상실 기준은 이사회의 의결에 의하여 결정한다.<br>
						<strong>제18조(제명)</strong> <br>
						① 제17조 제1항 제3호의 제명은 다음 각 호의 1에 해당하는 자에 대하여 총회의 의결을 거쳐 행한다.<br>
						1. 출자의 납입, 조합비, 특별조합비, 사용료, 수수료의 부담 그밖에 조합에 대한 의무를 이행 하지 아니한 조합원<br>
						2. 조합의 사업을 방해하거나 또는 방해할 목적의 행위를 한 조합원<br>
						3. 정관ㆍ제 규약ㆍ제 규정ㆍ법령 또는 법령에 의한 감독관청의 처분에 위반한 조합원<br>
						4. 고의 또는 중대한 과실로 인하여 조합에 경제적인 손실을 끼치거나 조합의 신용을 잃게 하고 명예를 훼손케 한 조합원<br>
						② 제1항의 경우 조합은 총회개최 10일 전에 해당 조합원에게 제명 사유를 통지하고, 총회에서 의견을 진술할 기회를 주어야 한다.<br>
						③ 제명은 제명하는 조합원에게 그 취지를 알리지 않으면 그 조합원에게 대항하지 못한다.<br>
						<strong>제19조(탈퇴자의 지분 환불과 그 정지)</strong><br>
						① 조합은 탈퇴한 조합원의 청구에 의하여 탈퇴한 날이 속하는 연도의 직전 사업년도말의 조합재산에 대하여 제20조에 의해 산출한 지분의 전액을 환불한다. <br>
						다만 조합원이 조합에 채무를 변제치 못하였을 때에는 사업년도 중일지라도 지분의 금액으로 채권·채무의 상계 또는 채무 변제에 이를 충당할 수 있다.<br>
						② 조합의 재산으로 조합의 채무를 완제하지 못하게 된 상황에서 탈퇴하는 조합원은 그 출자액의 한도 내에서 손실액을 부담하여야 한다.<br>
						③ 탈퇴한 조합원이 조합에 납입할 채무가 있을 때에는 제1항의 규정에 의한 지분의 환불을 정지할 수 있다.<br>
						④ 지분환불의 청구권은 탈퇴한 날로부터 2년간 행사하지 않으면 시효로 인하여 소멸된다.<br>

						<strong>제20조(지분의 범위)</strong> <br>
