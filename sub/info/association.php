						<strong>제53조(전무이사의 직무)</strong><br>
						① 전무이사는 이사장을 보좌하며 조합의 업무를 집행하고 이사장이 궐위되거나 부득이한 사유로 직무를 수행할 수 없는 때에는 이사장의 직무(총회 소집 및 그 의장의 직무를 제외한다)를 대행한다.<br>
						② 이사장, 전무이사 모두 궐위 또는 부득이한 사유로 직무를 수행할 수 없는 때에는 이사회에서 이사장의 직무를 대행할 자를 이사 중에서 선출한다. 이 경우 이사회는 이사 중 연장자 순으로 소집한다.<br>
						<strong>제54조(전무이사의 궐위)</strong> 전무이사가 궐위되거나 부득이한 사유로 직무를 수행할 수 없는 때에는 조합의 실장,부장급 또는 조합원이 아닌 자 중에서 이사장이 지명하는 자가 그 권한을 대행한다.<br>
						<strong>제55조(감사의 직무)</strong><br>
						① 감사는 조합의 재산과 업무집행 상황을 감사하여 총회에 보고하여야 한다.<br>
						② 감사는 조합의 재산상황 또는 업무집행에 관하여 부정한 사실을 발견한 때에는 이를 총회, 중앙회와 주무관청에 보고하여야 한다.<br>
						③ 조합이 이사장 또는 이사와 계약을 할 때에는 감사가 조합을 대표하며, 조합과 이사장 또는 이사간의 소송에 관하여도 또한 같다.<br>
						④ 감사는 조합의 회계장부, 서류의 열람 또는 등본을 할 수 있고 필요한 경우 이사장에 대하여 회계에 관한 보고를 요구할 수 있다.<br>
						⑤ 감사는 직무를 수행하기 위하여 필요하다고 인정할 때에는 조합의 업무 또는 재산에 관한 상황을 조사할 수 있다.<br>
						⑥ 감사는 조합 회계 및 업무집행 사항에 대해 직무 집행 상 필요시 외부감사를 시행할 수 있다. 단, 외부감사인은 주식회사의 외부감사에 관한법률 제3조(감사인)제1항 제 1호의 규정에 의한다.<br>
						<strong>제56조(임원의 책임)</strong><br>
						① 이사장, 이사, 전무이사 및 감사가 임무를 해태한 때에는 민법 제65조의 규정을 준용한다.<br>
						② 이사장, 전무이사 또는 감사가 총회 및 이사회의 의결 또는 의사에 반하여 조합에 손해를 끼쳤을 때에는 조합에 대하여 손해배상의 책임을 진다. 결산 보고서(1. 사업보고서, 2. 대차대조표, 3. 잉여금 처분 안, 4. 손익계산서)등 중요한 사항에 관하여 허위의 기재를 하거나 허위의 등기 또는 공고를 한 때에도 또한 같다.<br>
						③ 이사장, 전무이사 또는 감사가 직무를 수행함에 있어 불공정한 처리 또는 중대한 과실로 인하여 타인에게 손해를 끼쳤을 때에는 당해 임원은 배상의 책임을 진다.<br>
						④ 이사회의 불법행위 또는 중대한 과실로 인하여 조합에 손해를 끼쳤을 때에는 당해 불법행위 또는 중대한 과실에 관련된 이사회에 출석하여 그 의결에 가담한 이사는 조합에 대하여 연대하여 손해배상의 책임을 진다.<br>
						⑤ 제2항 내지 제4항의 구상권 행사는 이사, 전무이사에 대하여는 이사장이, 이사장, 이사, 전무이사의 전원에 대하여는 감사가, 이사장, 이사, 전무이사, 감사 전원에 대하여는 조합원 3분의1 이상의 동의를 얻어 조합원 대표가 이를 행한다.<br>
