						③ ‘중소기업자 외의 자’라 할지라도 출판업을 영위하는 자로서 법 시행령 제4조 규정의 범위 내에서 
						조합사업의 발전에 필요하다고 인정하여 조합원 5인 이상의 추천을 받은 자는 이사회 의결을 거쳐 조합원이 될 수 있다.<br>
